-- SQL SHOW Examples - Displaying database object information
-- This file demonstrates SHOW commands for exploring database structure

-- SHOW COMMAND SYNTAX:
-- SHOW DATABASES;                    -- List all databases
-- SHOW SCHEMAS;                     -- List all schemas in current database
-- SHOW TABLES;                      -- List all tables in current schema
-- SHOW COLUMNS IN <table_name>;     -- List all columns in a table

-- Example 1: SHOW DATABASES - List all available databases
-- This displays all databases you have access to
SHOW DATABASES;

-- Example 2: SHOW TABLES - List all tables in current schema
-- This displays all tables in the currently selected schema
-- Note: Results will vary based on your current schema context
SHOW TABLES;
